-- 6) 重複回答の確実な削除（同一question_id×同一body → idが小さい方を残し他を削除）
DELETE FROM qa_answers a USING qa_answers b
WHERE a.question_id = b.question_id
  AND a.body = b.body
  AND a.id <> b.id
  AND a.id > b.id;

-- 確認：各質問の回答件数（重複していれば2以上が出る）
SELECT q.title, count(a.id) AS answers
FROM qa_questions q JOIN qa_answers a ON a.question_id = q.id
GROUP BY q.id, q.title ORDER BY answers DESC;
